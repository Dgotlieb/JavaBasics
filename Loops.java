public class Loops {
    public static void main(String[] args) {
        for (int i = 0; i < 5; i++){
            System.out.println(i);
        }
        
        int a = 5;
        while (a < 10){
            System.out.println(a);
            a++;
        }
        
        int b = 8;
        do {
            System.out.println(b);
            b++;
        }while (b < 10);
    }
}
